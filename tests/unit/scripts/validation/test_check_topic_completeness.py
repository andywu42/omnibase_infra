@pytest.mark.unit
class TestCheckCompleteness:
    """Integration tests for the check_completeness function."""

    def test_registered_topic_passes(self, tmp_path: Path) -> None:
        # Create a constants file with the topic
        constants = tmp_path / "topics.py"
        constants.write_text('TOPIC = "onex.evt.platform.node-registration.v1"\n')

        # Create source that references the same topic
        src = tmp_path / "module.py"
        src.write_text('MY_TOPIC = "onex.evt.platform.node-registration.v1"\n')

        unregistered, registry = check_completeness(src_root=tmp_path)
        assert len(unregistered) == 0
        assert "onex.evt.platform.node-registration.v1" in registry

    def test_unregistered_topic_fails(self, tmp_path: Path) -> None:
        # Source with a topic that is NOT in any registry
        src = tmp_path / "module.py"
        src.write_text('MY_TOPIC = "onex.evt.fake.unknown-topic.v1"\n')

        unregistered, _ = check_completeness(src_root=tmp_path)
        assert len(unregistered) == 1
        assert unregistered[0][1] == "onex.evt.fake.unknown-topic.v1"

    def test_allowlisted_topic_passes(self, tmp_path: Path) -> None:
        # Source with a topic that is NOT in registry but IS in allowlist
        src = tmp_path / "module.py"
        src.write_text('MY_TOPIC = "onex.evt.fake.allowed-topic.v1"\n')

        allowlist = tmp_path / "allowlist.yaml"
        allowlist.write_text("allowlist:\n  - onex.evt.fake.allowed-topic.v1\n")

        unregistered, _ = check_completeness(
            src_root=tmp_path, allowlist_path=allowlist
        )
        assert len(unregistered) == 0

    def test_contract_registered_topic_passes(self, tmp_path: Path) -> None:
        # Create contract with topic
        contracts = tmp_path / "contracts"
        contracts.mkdir()
        contract = contracts / "contract.yaml"
        contract.write_text(
            textwrap.dedent("""\
            name: test_node
            event_bus:
              subscribe_topics:
                - onex.evt.platform.test-event.v1
            """)
        )

        # Create source referencing the same topic
        src = tmp_path / "src"
        src.mkdir()
        module = src / "module.py"
        module.write_text('TOPIC = "onex.evt.platform.test-event.v1"\n')

        unregistered, registry = check_completeness(
            src_root=src, contracts_root=contracts
        )
        assert len(unregistered) == 0
        assert "onex.evt.platform.test-event.v1" in registry


@pytest.mark.unit
class TestMainCLI:
    """Tests for the CLI entry point."""

    def test_clean_source_returns_zero(self, tmp_path: Path) -> None:
        # Create a registered topic
        topics_file = tmp_path / "topics.py"
        topics_file.write_text('T = "onex.evt.platform.node-registration.v1"\n')

        # Create source using the same topic
        module = tmp_path / "module.py"
        module.write_text('USE = "onex.evt.platform.node-registration.v1"\n')

        result = main(["--src", str(tmp_path), "--quiet"])
        assert result == 0

    def test_unregistered_returns_one(self, tmp_path: Path) -> None:
        module = tmp_path / "module.py"
        module.write_text('USE = "onex.evt.fake.unknown.v1"\n')

        result = main(["--src", str(tmp_path), "--quiet"])
        assert result == 1

    def test_show_registry(self, tmp_path: Path) -> None:
        topics_file = tmp_path / "topics.py"
        topics_file.write_text('T = "onex.evt.platform.test.v1"\n')

        result = main(["--src", str(tmp_path), "--show-registry"])
        assert result == 0

    def test_nonexistent_src_returns_two(self) -> None:
        result = main(["--src", "/nonexistent/path/to/src"])
        assert result == 2
