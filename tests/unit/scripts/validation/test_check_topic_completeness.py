@pytest.mark.unit
class TestCollectRegistryFromContracts:
    """Tests for contract YAML topic extraction."""

    def test_extracts_subscribe_topics(self, tmp_path: Path) -> None:
        contract = tmp_path / "node_a" / "contract.yaml"
        contract.parent.mkdir()
        contract.write_text(
            textwrap.dedent("""\
            name: node_a
            event_bus:
              subscribe_topics:
                - onex.evt.platform.node-registration.v1
                - topic: onex.cmd.platform.request-introspection.v1
              publish_topics:
                - onex.evt.platform.node-heartbeat.v1
            """)
        )
        topics = collect_registry_from_contracts(tmp_path)
        assert "onex.evt.platform.node-registration.v1" in topics
        assert "onex.cmd.platform.request-introspection.v1" in topics
        assert "onex.evt.platform.node-heartbeat.v1" in topics

    def test_extracts_consumed_published_events(self, tmp_path: Path) -> None:
        contract = tmp_path / "node_b" / "contract.yaml"
        contract.parent.mkdir()
        contract.write_text(
            textwrap.dedent("""\
            name: node_b
            consumed_events:
              - topic: onex.evt.omniclaude.session-outcome.v1
                name: onex.cmd.omniintelligence.session-outcome.v1
            published_events:
              - topic: onex.evt.platform.node-introspection.v1
            """)
        )
        topics = collect_registry_from_contracts(tmp_path)
        assert "onex.evt.omniclaude.session-outcome.v1" in topics
        assert "onex.cmd.omniintelligence.session-outcome.v1" in topics
        assert "onex.evt.platform.node-introspection.v1" in topics

    def test_nonexistent_root_returns_empty(self, tmp_path: Path) -> None:
        topics = collect_registry_from_contracts(tmp_path / "nonexistent")
        assert topics == set()

    def test_empty_contract_returns_empty(self, tmp_path: Path) -> None:
        contract = tmp_path / "contract.yaml"
        contract.write_text("name: empty_node\n")
        topics = collect_registry_from_contracts(tmp_path)
        assert topics == set()


