@pytest.mark.unit
class TestScanSourceForTopics:
    """Tests for Python source scanning."""

    def test_finds_topic_literals(self, tmp_path: Path) -> None:
        src = tmp_path / "module.py"
        src.write_text(
            textwrap.dedent("""\
            TOPIC = "onex.evt.platform.node-registration.v1"
            other = "not a topic"
            another = "onex.cmd.omniclaude.test-thing.v1"
            """)
        )
        results = scan_source_for_topics(tmp_path)
        topics = {t for _, t, _ in results}
        assert "onex.evt.platform.node-registration.v1" in topics
        assert "onex.cmd.omniclaude.test-thing.v1" in topics
        assert "not a topic" not in topics

    def test_excludes_tests_directory(self, tmp_path: Path) -> None:
        tests_dir = tmp_path / "tests"
        tests_dir.mkdir()
        src = tests_dir / "test_foo.py"
        src.write_text('TOPIC = "onex.evt.platform.node-registration.v1"\n')
        results = scan_source_for_topics(tmp_path)
        assert len(results) == 0

    def test_excludes_generated_files(self, tmp_path: Path) -> None:
        src = tmp_path / "generated_enum.py"
        src.write_text(
            textwrap.dedent("""\
            # GENERATED by codegen tool
            TOPIC = "onex.evt.platform.node-registration.v1"
            """)
        )
        results = scan_source_for_topics(tmp_path)
        assert len(results) == 0

    def test_handles_syntax_errors(self, tmp_path: Path) -> None:
        src = tmp_path / "bad.py"
        src.write_text("def foo(\n")  # syntax error
        results = scan_source_for_topics(tmp_path)
        assert len(results) == 0


